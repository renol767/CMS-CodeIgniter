<p class="fs-2" text-md-start style="font-size: 18"> Perancah (scaffolding) atau steger merupakan konstruksi pembantu pada pekerjaan bangunan gedung.
                Perancah dibuat apabila pekerjaan bangunan gedung sudah mencapai ketinggian 2 meter dan tidak dapat dijangkau oleh pekerja. Perancah adalah work platform sementara.
                Perancah (scaffolding) adalah suatu struktur sementara yang digunakan untuk menyangga manusia dan material dalam konstruksi atau perbaikan gedung dan bangunan-bangunan besar lainnya.
                Biasanya perancah berbentuk suatu sistem modular dari pipa atau tabung logam, meskipun juga dapat menggunakan bahan-bahan lain. Di beberapa negara Asia seperti RRC dan Indonesia, bambu
                masih digunakan sebagai perancah<br><br>
